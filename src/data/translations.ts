// دالة مساعدة للحصول على اللغة المحفوظة
export function getStoredLanguage(): Language {
  if (typeof window !== 'undefined') {
    return (localStorage.getItem('appLanguage') as Language) || 'ar';
  }
  return 'ar';
}

// دالة مساعدة للحصول على الترجمة
export function t(section: keyof typeof translations, key: string, lang: Language): string {
  const sectionData = translations[section];
  if (sectionData && sectionData[lang]) {
    return (sectionData[lang] as Record<string, string | string[]>)[key] as string || key;
  }
  return key;
}
